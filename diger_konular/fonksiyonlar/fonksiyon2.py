# parametreli fonksiyonlar
# a ve b fonksiyonun parametresidir
# istediğimiz kadar parametre ekleyebiliriz.

# toplama(a,b), fonksiyonun imzası - signature

def toplama(a,b):
    sonuc = a + b
    print(sonuc)

c = 11
d = 20

toplama(c,d)
toplama(45,78)

def toplama2(x, y, z):
    sonuc = x + y + z
    print(sonuc)

toplama2(56,78,41)
